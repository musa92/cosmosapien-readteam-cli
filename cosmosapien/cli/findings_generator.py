@app.command()
def validate(
    file_path: str = typer.Argument(..., help="JSON file to validate")
):
    """Validate a findings JSON file against the schema."""
    
    try:
        with open(file_path, 'r') as f:
            data = json.load(f)
        
        finding = VulnerabilityFinding(**data)
        console.print(f"[green]Valid finding: {finding.issue_title}[/green]")
        
        # Show summary
        table = Table(title="Finding Summary")
        table.add_column("Field", style="cyan")
        table.add_column("Value", style="green")
        
        table.add_row("Issue Title", finding.issue_title)
        table.add_row("Model", f"{finding.model.name} ({finding.model.version})")
        table.add_row("Provider", finding.model.provider)
        table.add_row("Topic Area", finding.issue_summary.topic_area)
        table.add_row("Severity", finding.issue_summary.self_assessed_severity)
        table.add_row("Steps", str(len(finding.steps_to_reproduce)))
        
        console.print(table)
        
    except Exception as e:
        console.print(f"[red]Validation failed: {e}[/red]")
        raise typer.Exit(1)


